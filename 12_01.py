print(a.title())
print(a.capitalize())
print(a.swapcase())
print(a.replace("String","Name"))
print(a.count("a"))
print(a.find("Dishant"))
print(a[a.find("is") : ])
print(a.split("is"))
print(a.strip())
print(a.lstrip())
print(a.rstrip())
print(a.isalpha())
print(a.isdigit())
print(a.startswith("M"))
print(a.endswith("t"))

